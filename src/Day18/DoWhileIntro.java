package Day18;

public class DoWhileIntro {
    public static void main(String[] args) {
        // count from 1 to 10
 int x = 1 ;
 do {
     System.out.println(x);
     ++x;
 } while (x<=5);
        System.out.println("_____________");
 // count 5 to 1
        int y = 5 ;
        do {
            System.out.println(y);
            --y;
        } while (y>0);

    }
}
