package Day18;

public class ForLoopIntro {
    public static void main(String[] args) {
        for ( int i = 1 ; i<=5 ; i++){
            System.out.println("i= " + i);
            System.out.println("Happy Birthday Serdar");
        }
        for (int counter = 5 ; counter>0; counter--){
            System.out.println("counter" + counter);
        }
    }
}
