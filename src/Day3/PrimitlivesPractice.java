package Day3;

public class PrimitlivesPractice {
    public static void main(String[] args) {
        byte letterCount = 26;
        System.out.println ("The letter count is: " +letterCount);
        short sheepCount = 300;
        System.out.println ("Sheep price:         " +sheepCount);
        int catCount = 20;
        System.out.println ("The int count is:    " +catCount);
        long mileToSun = 1000000;
        System.out.println ("Sun is:              " +mileToSun);
        // floating point
        // you must add F or f end of the indicate this is float data type
        float priceOfBanana = 1.99f;
        System.out.println ("Banana price:        " +priceOfBanana);
        float priceOfPotato = 2.49F;
        System.out.println ("Potato price:        " +priceOfPotato);
        //larger floating point numbers
        double priceOfIpad1 = 355.99d;
        System.out.println ("Ipad1 price:         " +priceOfIpad1);
        double priceOfIpadPro = 1024.99D;
        System.out.println ("IpadPro price:       " +priceOfIpadPro);
        //compilar automatically assume it's not required to have  d
        double priceOfMac = 299.99;
        System.out.println ("Mac price:           " +priceOfMac);


    }
}
